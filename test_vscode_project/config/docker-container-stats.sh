# Container Stats

# Saat menjalankan beberapa container, di sistem Host / Local komputer
# pemakaian resource seperti CPU dan Memory hanya terlihat digunakan oleh Docker saja.
# Terkadang kita ingin melihat detail dari pemakaian resource untuk tiap container-nya.
# Docker memiliki kemampuan untuk menampilkan statistik pemakaian alokasi memory oleh container yang berjalan

# Perintah untuk menampilkan statistik pemakaian memory
docker container stats
