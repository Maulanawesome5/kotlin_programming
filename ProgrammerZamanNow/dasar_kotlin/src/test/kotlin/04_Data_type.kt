fun main() {
    /*
    * Tipe data Character -> berisi satu elemen tunggal, diapit tanda ' petik satu '
    * Tipe data Boolean -> berisi nilai True dan False
    * */
    val a: Char = 'A'
    val j: Char = 'J'
    val i: Char = 'I'

    val benar: Boolean = true
    val salah: Boolean = false

    print(a)
    print(j)
    print(i)

    println()
    println(benar)
    println(salah)
}
